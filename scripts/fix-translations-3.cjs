function set(obj, path, value) {
  const parts = path.split('.');
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    cur = cur[parts[i]];
  }
  cur[parts[parts.length - 1]] = value;
}

for (const [key, val] of Object.entries(enFixes)) {
  set(en, key, val);
}

for (const [key, val] of Object.entries(arFixes)) {
  set(ar, key, val);
}

